    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

      try {
            PreparedStatement insertarDatos = cn.prepareStatement("INSERT INTO producto(existencia,preciostock,nombre,marca,preciocompra) VALUES(?,?,?,?,?)");
            insertarDatos.setString(1, jTextField2.getText());
            insertarDatos.setString(2, jTextField5.getText());
            insertarDatos.setString(3, jTextField1.getText());
            insertarDatos.setString(4, jTextField3.getText());
            insertarDatos.setString(5, jTextField4.getText());
            insertarDatos.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados exitosamente.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Ingresoinventario.class.getName()).log(Level.SEVERE, null, ex);
        }  
        
    }//GEN-LAST:event_jButton1MouseClicked

