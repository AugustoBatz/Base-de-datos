    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        try {
            PreparedStatement ActualizarProductos = cn.prepareStatement("UPDATE producto SET nombre = '"+jTextField1.getText()+"', preciostock = '"+jTextField2.getText()+"', marca = '"+jTextField3.getText()+"', existencia = '"+jTextField4.getText()+"', preciocompra = '"+jTextField5.getText()+"' WHERE idprod = '"+ID+"' ");
            ActualizarProductos.executeUpdate();

        } catch (SQLException ex) {
            Logger.getLogger(Compras.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Datos actualizados.");
        
        Inventario in = new Inventario();
        in.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_jButton1MouseClicked

